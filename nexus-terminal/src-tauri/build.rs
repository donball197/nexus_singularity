use std::fs;
use std::path::Path;

fn main() {
    let icon_path = Path::new("icons/icon.png");
    
    // Fixed: The array is exactly 69 bytes
    if !icon_path.exists() {
        let minimal_rgba_png: [u8; 69] = [
            0x89, 0x50, 0x4E, 0x47, 0x0D, 0x0A, 0x1A, 0x0A, 0x00, 0x00, 0x00, 0x0D,
            0x49, 0x48, 0x44, 0x52, 0x00, 0x00, 0x00, 0x20, 0x00, 0x00, 0x00, 0x20,
            0x08, 0x06, 0x00, 0x00, 0x00, 0x73, 0x7A, 0x7A, 0xF4, 0x00, 0x00, 0x00,
            0x0C, 0x49, 0x44, 0x41, 0x54, 0x78, 0xDA, 0x63, 0x60, 0x60, 0x60, 0x00,
            0x00, 0x00, 0x04, 0x00, 0x01, 0x27, 0x34, 0x27, 0x0A, 0x00, 0x00, 0x00,
            0x00, 0x49, 0x45, 0x4E, 0x44, 0xAE, 0x42, 0x60, 0x82,
        ];
        
        fs::create_dir_all("icons").unwrap();
        fs::write(icon_path, minimal_rgba_png).expect("FORGE_FAILURE: Could not create DNA asset");
        println!("cargo:warning=[FORGE] Icon DNA regenerated successfully.");
    }
    
    tauri_build::build();
}
